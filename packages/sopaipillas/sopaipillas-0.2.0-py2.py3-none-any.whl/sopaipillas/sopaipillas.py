"""Main module."""


def ingredients(count):
    """Prints ingredients for making `count` sopaipillas."""
    print(f'{4*count} cups all-purpose flour')
    print(f'{2*count} teaspoons baking powder')
    print(f'{count} teaspoons salt')
    print(f'{4*count} tablespoons shortening')
    print(f'{1.5*count} cups warm water')
    print(f'{2*count} quarts oil for frying')
